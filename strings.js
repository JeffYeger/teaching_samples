// Strings: a group of characters inside quotation marks
let msg = "hello";
msg = "asmin12344";
msg = "passW0rd!!";
msg = "habad";
msg = "it's ok";

// String concatenation: combining strings with the + operator
let str = "yehuda" + 8; // Result: "yehuda8"

// String chaining example: combining multiple values into a string
let result = 8 + "u" + 19 + "ty"; // Result: "8u19ty"

// Numeric values in strings still participate in math operations
let result1 = "8" - 2; // Result: 6

// Strings containing numbers will be treated as numeric values
let result2 = "8" * "8"; // Result: 64
